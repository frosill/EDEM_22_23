'''
AREA TRIANGULO
'''

def area_triangulo(base: float, altura: float) -> float:

  return print(base*altura/2)

'''
AREA CIRCULO
'''

from math import pi

def area_circulo(radio: float) -> float:
  
  return print(pi*radio**2)
  